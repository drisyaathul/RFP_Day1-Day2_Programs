package com.bridgelabz;

//Write a program to check two strings are equal or not.

public class StringEqual {
    public static void main(String[] args) {
        String a = "Mango";
        String b = "Mango";
        if (a.equals(b)) {
            System.out.println("String 'a' is equal to string 'b'");
        } else {
            System.out.println("String 'a' is not equal to string 'b'");
        }
    }
}

